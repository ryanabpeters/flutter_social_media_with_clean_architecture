import 'package:flutter/material.dart';

/// Resource class for storing app level color constants.
class AppColors {
  /// The default colors of general app components.

  /// The app bar color.
  static const Color appBarColor = Colors.transparent;

  /// The app bottom bar color.
  static const Color appBottomBarColor = Colors.black;

  /// The app scaffold color.
  static const Color appScaffoldColor = Colors.black;

  /// The default icon color.
  static const Color iconColor = Colors.white;

  /// The default text color.
  static const Color textColor = Colors.white;

  /// The text field colors.

  /// The color when enabled.
  static const Color textFieldBorderColorEnabled = Colors.grey;

  /// The color when there is an error.
  static const Color textFieldBorderColorError = Colors.red;

  /// The color when focused.
  static const Color textFieldBorderColorFocused = Colors.black;

  /// The default color of the field.
  static const Color textFieldFillColor = Colors.white;
}
